       <h2><a id="Bleeding"> Bleeding</a></h2>
       <h3>The most common type of pet emergency first aid is the treatment of cuts and wounds. Here s what to do:<br/><br/>1. Locate where the blood is coming from by gently feeling over your pet with clean hands (or use nitrile gloves).<br/>2. When you have located where the bleeding is coming from, check whether any foreign objects are sticking out of the wound. <br/>You might be able to remove something if it is small with tweezers as long as you don t need to insert them into the wound (never do this).<br/>3. If you can, clean the wound gently with a sterile wipe or run sterile solution over it and pat it dry.<br/>4. Apply firm pressure using a clean gauze swab or clean towel if you don t have any gauze swabs.<br/>5. Wrap a bandage around the area to hold the dressing in place.<br/>6. Do not remove the bandage as it could interfere with any clots and can cause the bleeding to start again.<br/>7. Call your vet and arrange to take your pet in for treatment.<br/><br/>  </h3>
       <h2><a id="Choking"> Choking</a></h2>
       <h3>If a pet is choking keep an eye on them until they stop. Most pets will easily cough something up and audible choking is a sign that they are still breathing. <br/>If they continue to choke or stop breathing you should intervene with these steps:<br/><br/>1. Open the animal s mouth if you can and do a finger sweep inside to remove any objects.<br/>2. Try to see inside and check whether anything can be removed.<br/> Don t push your fingers down their throat to grab something, you could push it further down. <br/>Using tweezers is helpful if something is slippery and difficult to remove with your fingers. <br/>3. Perform the Heimlich manoeuvre on your pet if you feel you can. <br/>To do this position your pet s back against your stomach and put your fist below their ribcage. <br/>Gently pull in and upwards with your fist in two to three movements to try and dislodge the obstruction.<br/></br></h3>
        <h2>Fractures and Broken Bones</h2><h3>You may notice a fracture or broken bone through a swollen or misshapen limb. The animal could have limited mobility and be limping, or not moving at all. <br/>Arrange to take your pet to the vet. Gently put them in a box padded with blankets and keep them warm. Pad the injured area with blankets to support them on the journey to the vet.<br/>Do not bandage or splint a broken or fractured bone. This could make the injury worse.<br/> </h3> 
        <h2><br/>Collapsing and Unconsciousness</h2>
        <h3>1. Check it’s safe to approach the animal. If they have been electrocuted, take care not to step into water or touch them when you’re not certain they’ve lost contact with the current.<br/>2. Call out your pets name and pat them gently to see if they respond.<br/>3. Check whether your pet is breathing by holding some fur under its nose and mouth.<br/>4. Perform CPR if you think you can and your pet is not breathing and there is no heartbeat.<br/>5. Keep your pet warm, a foil blanket is especially useful. <br/>6. Call your vet and arrange for them to see your pet.<br/><br/><br/><br/></h3>
